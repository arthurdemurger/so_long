#include "../../inc/so_long.h"

void	ft_putnbr(int n)
{
	long	ln;

	ln = n;
	if (n < 0)
	{
		ln *= -1;
		ft_putchar('-');
	}
	if (ln > 9)
		ft_putnbr(ln / 10);
	ft_putchar(ln % 10 + 48);
}
